// Operator JavaScript
// dalam ini kita belajar tentang operator penugasan

// operator penugasan (=)
let x = 5;

// operator penjumlahan (+)
let x = 5;
let y = 10;
let z = x + y;

// operator perkalian (*)
let x = 5;
let y = 10;
let z = x * y;

// operator aritmatika
// +, -, *, /, **, %, ++, --, 

let a = 7;
let b = 2;
let c = a % b;
// dan lain lain


// tanda + bisa digunakan selain untuk penjumlahan angka dpaat juga buat teks

let text1 = "jhon";
let text2 = "morgan";
let text3 = text1 + "" + text2;

// operator += penugasan juga dapat digunakan untuk menambahkan string
let text1 = "halo bang";
text1 += "semoga sehat selalu";

// menjumlahkan bilangan dan angka
let x = 5 + 5; // 10
let y = "5" + 5; // 55
let z = "hello" + 5; // hello5

// contoh lain
let x = 10;
x += 5; // hasilnya 15

// catatan
// += | x += y | x = x + y
// -= | x -= y | x = x - y
// *= | x *= y | x = x * y
// /= | x /= y | x = x / y
// %= | x %= y | x = x % y
// **= | x **= y | x = x ** y