// function parameter

// kita bisa mengirim informasi ke function yang ingin kita panggil 
// untuk melakukan hal tersebut, kita perlu menambahkan parameter atau argument di function yang sudah kita buat

// parameter ditempatkan di dalam kurung diklarasikan method
// parameter bisanya lebih dar satu, jika labih dari satu, harus dipisah menggunakan tanda koma 

function sayHello(firstName, lastName) {
    console.log(`hello ${firstName} ${lastName}`);
}

sayHello("hexs", "project");
sayHello("budiono", "siregar");
